    fun translateCountry(name: String?): String? {
        if (name.isNullOrBlank()) return name
        val key = countryKeyByEnglishName[name.trim().lowercase()] ?: return name
        return t(key)
    }

    fun updatePage() {
        val elements = document.querySelectorAll("[data-i18n]")
        for (i in 0 until elements.length) {
            val el = elements.item(i)?.unsafeCast<Element>() ?: continue
            val key = el.getAttribute("data-i18n") ?: continue
            el.textContent = t(key)
        }
    }

    fun updateActiveLangOption() {
        val options = document.querySelectorAll(".lang-option")
        for (i in 0 until options.length) {
            val el = options.item(i)?.unsafeCast<Element>() ?: continue
            val langAttr = el.getAttribute("data-lang") ?: continue
            if (langAttr == currentLang) {
                el.classList.add("active")
                // Update the dropdown button text with the flag
                val btn = document.querySelector(".lang-dropbtn")?.unsafeCast<HTMLElement>()
                btn?.textContent = getFlag(langAttr)
            } else {
                el.classList.remove("active")
            }
            // Add click handler to change language
            el.addEventListener("click", { _ ->
                val lang = el.getAttribute("data-lang") ?: return@addEventListener
                window.localStorage.setItem("preferredLanguage", lang)
                loadLang(lang).then {
                    updatePage()
                    updateActiveLangOption()
                }
            })
        }
    }

    fun getFlag(lang: String): String = langEmojis[lang] ?: "\uD83C\uDDFA\uD83C\uDDF8"
